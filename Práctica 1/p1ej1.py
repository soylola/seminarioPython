#Escribe un programa que solicite al usuario su año de nacimiento y muestre en qué año cumplirá 18, 21 y 100 años.

birth_year = int(input('Enter your birth year: '))
print(f"You are going to have 18 in {birth_year + 18}")
print(f"You are going to have 21 in {birth_year + 21}")
print(f"You are going to have 100 in {birth_year + 100}")